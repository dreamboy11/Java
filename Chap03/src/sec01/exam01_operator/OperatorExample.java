package sec01.exam01_operator;

public class OperatorExample {
	public static void main(String[] args) {
		int x = 100;
		int y = 10;
		int z = 90;
		
		System.out.println(x + y - z);
		System.out.println(x - y - z);
		System.out.println(x + y + z);
		System.out.println(x == y);
		System.out.println(x == z);

	}

}
